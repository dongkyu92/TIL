package day1;

public class LiteralTest {

	public static void main(String[] args) {
		System.out.println(1+1);     //2 정수
		System.out.println(1.0+1);  //2.0 실수
		System.out.println('1'+1);   // 50 문자 (단일 인용부호)
		System.out.println("1"+1);  // 11 문자열
		System.out.println("123"+4); //127
		System.out.println(777777777777777777L); 
		

	}

}
